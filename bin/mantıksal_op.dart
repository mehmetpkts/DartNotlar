main() {

  // ! || , &&


  bool deneme = true;  // sonucu true olan bi şeyi ! kullanarak tam tersi olan false dönüştürmek için kullandık.
  print(!deneme);


  // veya operatörü : ||

  print((5>2) || (3>5));  // sol taraf true sağ taraf false ortadaki işaret de veya olduğu için sonucumuz true çıkmış oldu

  // ve operatörü : &&

  print((5>2) && (3>5)); //// sol taraf true sağ taraf false ortadaki işaret de ve olduğu için sonucumuz false çıkmış oldu


}